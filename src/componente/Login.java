    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String userName = labelUserName.getText(); // Obtiene el nombre de usuario del campo de texto
        String passwordUser = new String(labelPasswordUser.getText()); // Obtiene la contraseña del campo de texto
        boolean loginExitoso = false; // Indicar si el inicio de sesión fue exitoso o fallido

        if (userName.isEmpty() || passwordUser.isEmpty() || userName.equals("Ingresa tu nombre de usuario") || passwordUser.equals("*************")) {
            JOptionPane.showMessageDialog(null, "No puede haber un campo vacío."); // Muestra un mensaje si algún campo está vacío o tiene valores predeterminados
            return;
        }

        for (int i = 0; i < userDatabase.usercount; i++) {
            if (userDatabase.usernames.get(i).equals(userName) && userDatabase.passwords.get(i).equals(passwordUser)) {
                loginExitoso = true; // Indica que el inicio de sesión fue exitoso si se encuentra una coincidencia de nombre de usuario y contraseña en la base de datos
                break; // Sale del bucle una vez encontrada la coincidencia
            }
        }

        if (loginExitoso) {
            JOptionPane.showMessageDialog(null, String.format("Login exitoso, bienvenido %s", userName)); // Muestra un mensaje de bienvenida
            intentos = 0; // Reinicia el contador de intentos fallidos

            MenuPrincipal menuPrincipal = new MenuPrincipal(); // Crea e inicializa la ventana del menú principal
            menuPrincipal.setVisible(true); // Hace visible el menú principal
            menuPrincipal.setLocationRelativeTo(null); // Centra el menú principal en la pantalla
            this.dispose(); // Cierra la ventana de inicio de sesión
        } else {
            intentos++; // Incrementa el contador de intentos fallidos
            if (intentos >= MAX_INTENTOS) {
                JOptionPane.showMessageDialog(null, "Ha alcanzado el límite máximo de intentos. Vuelva más tarde.");
                btnLogin.setEnabled(false); // Deshabilita el botón de inicio de sesión
            } else {
                JOptionPane.showMessageDialog(null, String.format("Usuario y/o contraseña incorrecta. Intento fallido %d de %d", intentos, MAX_INTENTOS)); // Muestra un mensaje indicando el intento fallido
                labelPasswordUser.setText("*************"); // Restablece el campo de contraseña
                labelPasswordUser.setForeground(Color.gray); // Cambia el color del texto de la contraseña a gris
                labelUserName.setText("Ingresa tu nombre de usuario"); // Restablece el campo de nombre de usuario
                labelUserName.setForeground(Color.gray); // Cambia el color del texto del nombre de usuario a gris
            }
        }

    }//GEN-LAST:event_btnLoginActionPerformed

