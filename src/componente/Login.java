    private void showPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showPasswordActionPerformed
        if (showPassword.isSelected()) {
            labelPasswordUser.setEchoChar((char) 0); // Muestra la contraseña en texto plano
            if (labelPasswordUser.equals("*************")) {
                labelPasswordUser.setText(""); // Limpia el texto predeterminado si está presente
            }
        } else {
            labelPasswordUser.setEchoChar('*'); // Oculta la contraseña con asteriscos
        }
    }//GEN-LAST:event_showPasswordActionPerformed

