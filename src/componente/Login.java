    private void labelUserNameMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelUserNameMousePressed
        if (labelUserName.getText().equals("Ingresa tu nombre de usuario")) {
            // Verifica si el campo de nombre de usuario tiene el texto predeterminado "Ingresa tu nombre de usuario"
            labelUserName.setText(""); // Borra el texto predeterminado para permitir al usuario ingresar su nombre de usuario
            labelUserName.setForeground(Color.black); // Cambia el color del texto del campo de nombre de usuario a negro para indicar que está activo
        }

        if (String.valueOf(labelPasswordUser.getPassword()).isEmpty()) {
            // Verifica si el campo de contraseña está vacío.
            labelPasswordUser.setText("*************"); // Establece el texto predeterminado para ocultar visualmente la contraseña
            labelPasswordUser.setForeground(Color.gray); // Cambia el color del texto del campo de contraseña a gris para indicar que es un texto predeterminado
        }
    }//GEN-LAST:event_labelUserNameMousePressed

    private void labelPasswordUserMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelPasswordUserMousePressed
        if (String.valueOf(labelPasswordUser.getPassword()).equals("*************")) {
            // Verifica si el campo de contraseña tiene el texto predeterminado "*************"
            labelPasswordUser.setText(""); // Borra ese texto para permitir al usuario ingresar su contraseña
            labelPasswordUser.setForeground(Color.black); // Cambia el color del texto del campo de contraseña a negro para indicar que está activo
        }

        if (labelUserName.getText().isEmpty()) {
            // Verifica si el campo de nombre de usuario está vacío
            labelUserName.setText("Ingresa tu nombre de usuario"); // Establece el texto predeterminado
            labelUserName.setForeground(Color.gray); // Cambia el color del texto del campo de nombre de usuario a gris para indicar que es un texto predeterminado
        }
    }//GEN-LAST:event_labelPasswordUserMousePressed

